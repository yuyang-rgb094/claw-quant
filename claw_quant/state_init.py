def init_capital_registry() -> bool:
    """Initialize capital_allocation_registry.md if it doesn't exist."""
    if CAPITAL_ALLOCATION_REGISTRY_PATH.exists():
        logger.info("Capital allocation registry already exists: %s", CAPITAL_ALLOCATION_REGISTRY_PATH)
