"""State initialization — populates state files with initial data.

Run once before first deployment to ensure all state files exist
with valid initial data. Idempotent — safe to run multiple times.
"""

from __future__ import annotations

import logging
from datetime import datetime
from pathlib import Path

from claw_quant.config import (
    PROJECT_ROOT,
    FISHER_STATE_PATH,
    SFM_STATE_PATH,
    DAMODARAN_STATE_PATH,
    CAPITAL_ALLOCATION_REGISTRY_PATH,
    DATA_PROVIDER,
)

logger = logging.getLogger("claw_quant.state_init")


def init_all() -> dict[str, bool]:
    """Initialize all state files. Returns status per file."""
    results = {}

    results["fisher"] = init_fisher_state()
    results["sfm"] = init_sfm_state()
    results["damodaran"] = init_damodaran_state()
    results["capital"] = init_capital_registry()
    results["directories"] = init_directories()

    success = all(results.values())
    logger.info(
        "State initialization: %s (%d/%d files OK)",
        "SUCCESS" if success else "PARTIAL",
        sum(results.values()),
        len(results),
    )
    return results


